/**
 * Regenerates src/lib/auth/countries-data.ts from libphonenumber-js
 * metadata (Phase 0J): the country->dial-code table is a static fact, so
 * shipping the full 40 KB-gzip metadata to every phone screen just to
 * derive it was pure waste. Re-run after upgrading libphonenumber-js:
 *   node scripts/generate-countries.mjs
 */
import { writeFileSync } from "node:fs";
import { createRequire } from "node:module";
import { getCountries, getCountryCallingCode } from "libphonenumber-js/core";

const require = createRequire(import.meta.url);
const metadata = require("libphonenumber-js/metadata.min.json");

const pairs = getCountries(metadata)
  .map((iso) => [iso, getCountryCallingCode(iso, metadata)])
  .sort((a, b) => (a[0] < b[0] ? -1 : 1));

const body = `// AUTO-GENERATED by scripts/generate-countries.mjs - do not edit by hand.
// Source: libphonenumber-js metadata (regenerate after upgrading the dep).
// Static ISO -> dial-code pairs so the client never ships the metadata
// blob just to render the country picker (Phase 0J).

export const COUNTRY_DIAL_CODES: ReadonlyArray<readonly [string, string]> = [
${pairs.map(([iso, code]) => `  ["${iso}", "${code}"],`).join("\n")}
];
`;
writeFileSync("src/lib/auth/countries-data.ts", body);
console.log(`wrote ${pairs.length} countries to src/lib/auth/countries-data.ts`);
